package ClassesObjetos;

public class Aluno {

	// Atributos
	
    String Nome;
	String CPF;
	int Idade;
	String Matricula;
	
	// Metodos	
	public String getNome() {
		return Nome;
	}

	public void setNome(String Nome) {
		this.Nome = Nome;
	}

	public String getCPF() {
		return CPF;
	}

	public void setCPF(String cPF) {
		CPF = cPF;
	}

	public int getIdade() {
		return Idade;
	}

	public void setIdade(int idade) {
		Idade = idade;
	}

	public String getMatricula() {
		return Matricula;
	}

	public void setMatricula(String matricula) {
		Matricula = matricula;
	}

	// Construtor
	   public Aluno() {} // Default
	
	
	   
	   
	
	
}
